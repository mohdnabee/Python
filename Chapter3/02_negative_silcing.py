name = "Harry"
print(name[0:3])

print(name[-4:-1])
print(name[1:4])


#  Advance Sliccing Technique
print (name[:4])# is same as print(name[0:4])
print(name[1:]) # is same as print(name[1:5])
print(name[1:5])



# Slicing Skip value 
numbers =  "0123456789"
print (numbers[1:7:3])  # output 14

alphapahts = "ABCDEFGHIJKLMNOPQRSTUVWXYZ"
print(alphapahts[1:9:4]) # output ACEGIKMOQSUWY